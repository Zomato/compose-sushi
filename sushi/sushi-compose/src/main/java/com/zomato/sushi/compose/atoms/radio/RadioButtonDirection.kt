package com.zomato.sushi.compose.atoms.radio


/**
 * Defines the position of the radio button relative to its associated text and content.
 * 
 * - Start: Positions the radio button before the text (left side in LTR layouts)
 * - End: Positions the radio button after the text (right side in LTR layouts)
 *
 * @author gupta.anirudh@zomato.com
 */
enum class RadioButtonDirection {
    Start, End
}