package com.zomato.sushi.compose.atoms.switch


/**
 * Defines the position of the switch relative to its associated text and content.
 * 
 * - Start: Positions the switch before the text (left side in LTR layouts)
 * - End: Positions the switch after the text (right side in LTR layouts)
 *
 * @author gupta.anirudh@zomato.com
 */
enum class SwitchDirection {
    Start, End
}