package com.zomato.sushi.compose.atoms.checkbox


/**
 * @author gupta.anirudh@zomato.com
 *
 * Defines the position of the checkbox relative to its associated text and content.
 *
 * - Start: Positions the checkbox before the text (left side in LTR layouts)
 * - End: Positions the checkbox after the text (right side in LTR layouts)
 */
enum class CheckBoxDirection {
    Start, End
}