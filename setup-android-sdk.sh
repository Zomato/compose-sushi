#!/bin/bash

set -e  # Exit on any error

echo "ðŸ”§ Setting up Android SDK..."

# Function to check if command exists
command_exists() {
    command -v "$1" >/dev/null 2>&1
}

# Install Android SDK using brew (macOS)
if ! command_exists sdkmanager; then
    echo "ðŸ“¦ Installing Android SDK command line tools..."
    if command_exists brew; then
        brew install --cask android-commandlinetools
    else
        echo "âŒ Error: Homebrew not found. Please install Homebrew first."
        exit 1
    fi
else
    echo "âœ… Android SDK command line tools already installed"
fi

# Set up Android SDK environment variables
export ANDROID_HOME=$HOME/Library/Android/sdk
export ANDROID_SDK_ROOT=$ANDROID_HOME
export PATH=$PATH:$ANDROID_HOME/cmdline-tools/latest/bin:$ANDROID_HOME/platform-tools

echo "ðŸ“ Android SDK Home: $ANDROID_HOME"

# Create SDK directory if it doesn't exist
mkdir -p $ANDROID_HOME

# Accept licenses
echo "ðŸ“„ Accepting Android SDK licenses..."
yes | sdkmanager --licenses 2>/dev/null || echo "âœ… License acceptance completed"

# Install required SDK components
echo "ðŸ“¦ Installing Android SDK components..."
SDK_COMPONENTS=(
    "platform-tools"
    "platforms;android-34"
    "platforms;android-33"
    "build-tools;34.0.0"
    "build-tools;33.0.2"
    "cmdline-tools;latest"
)

for component in "${SDK_COMPONENTS[@]}"; do
    echo "Installing $component..."
    sdkmanager "$component" || echo "âš ï¸  Failed to install $component, continuing..."
done

# Create local.properties file
echo "ðŸ“ Creating local.properties file..."
cat > local.properties << EOF
## This file is automatically generated by setup-android-sdk.sh
# Do not modify this file manually in the repository.
# This file should *NOT* be checked into Version Control Systems,
# as it contains information specific to your local configuration.
#
# Location of the Android SDK. This is used by Gradle.
sdk.dir=$ANDROID_HOME
EOF

# Verify local.properties was created
if [ -f "local.properties" ]; then
    echo "âœ… local.properties created successfully"
    echo "Contents:"
    cat local.properties
else
    echo "âŒ Failed to create local.properties"
    exit 1
fi

# Export environment variables for the current shell session
echo ""
echo "ðŸŒ Setting up environment variables..."
echo "export ANDROID_HOME=$ANDROID_HOME"
echo "export ANDROID_SDK_ROOT=$ANDROID_HOME"
echo "export PATH=\$PATH:$ANDROID_HOME/cmdline-tools/latest/bin:$ANDROID_HOME/platform-tools"

# Verify SDK installation
echo ""
echo "ðŸ” Verifying Android SDK installation..."
if command_exists adb; then
    echo "âœ… ADB: $(adb --version | head -1)"
else
    echo "âš ï¸  ADB not found in PATH"
fi

if command_exists sdkmanager; then
    echo "âœ… SDK Manager: Available"
    echo "ðŸ“¦ Installed packages:"
    sdkmanager --list_installed | grep -E "(platforms|build-tools|platform-tools)" | head -10
else
    echo "âŒ SDK Manager not found"
fi

echo ""
echo "âœ… Android SDK setup completed successfully!"
echo "ðŸŽ¯ You can now run Gradle builds that require Android SDK"